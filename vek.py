vek = int(input("kolik ti je let?"))
if vek >=200:
    print("Jsi mimizemšťan.")
elif vek >=150:
    print("Ze které jseš planety?") 
elif vek >=100:
    print("Tady jsou pro vás nové zuby.")
elif vek >=50:
    print("Vám už je půl století.")
elif vek >=30:
    print("Vy už rozhodně máte řidičák.")        
elif vek >=18:
    print("Můžu nabídnout vodku, pivo nebo víno.")
elif vek >=7:
    print("Tady máte štávu kterou jste si objednal.") 
elif vek >=1:
    print("Můžeme nabídnout mléko, přesnídávku, čaj nebo vodu.")
elif vek >=0:
    print("Sunar už bohužel došel.")
else:
    print("Pro náštěvy z budoucnosti bohužel nemáme nic v nabídce.")        